    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try{
            boolean resolt = this.controler.transfer(Integer.parseInt(jTextField1.getText()), 
            Integer.parseInt(jTextField4.getText()), 
            Double.parseDouble(jTextField3.getText()));
            if(resolt == true){
                this.jLabel1.setText("Переказ здійнено успішно.");
            }
        }
        catch(ToLowBalanceException e){       
            this.jLabel1.setText("Не коректне значеня суми переказу.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

