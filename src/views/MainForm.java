    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        BankAccount bankAccount = this.controler.findAccountById(Integer.parseInt(jTextField1.getText()));
        if(bankAccount !=null){
            this.jTextField2.setText(bankAccount.getBalance() + "");
            this.jTextField4.setEditable(true);
            this.jComboBox1.setEnabled(true);
            this.jTextField3.setEditable(true);
        }
        else {
            this.jLabel1.setText("Не вірний номер.");
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

